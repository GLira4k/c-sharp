/*
    Operadores de Comparação
    
    Igual: ==
    Diferente: != 
    Maior que: >
    Menor que: <
    Maior ou igual a: >=
    Menor ou igual a: <=

    Podemos comparar qualquer tipo de dado.

    A comparação sempre retorna True ou False.


*/

using System;

namespace MeuApp
{
    public class Program
    {
        public static void Main(string[] args)
        {
            int x = 25;

            x == 0;
            x != 0;
            x > 0;
            x < 0;
            x <= 0;
            x >= 0;
        }
    }
}